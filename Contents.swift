func countConsonants(in word: String) -> Int {
    let consonants = "bcdfghjklmnpqrstvwxyz" + "BCDFGHJKLMNPQRSTVWXYZ"
    var consonantsCount = 0
    for character in word {
        if consonants.contains(character) {
            consonantsCount += 1
        }
    }
    return consonantsCount
}

